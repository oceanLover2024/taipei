export function renderCard() {
  let main = document.querySelector("main");
  let line = document.createElement("div");
  line.setAttribute("class", "line");
  let mainLayout = document.createElement("div");
  mainLayout.setAttribute("class", "main_layout");
  let cardInfoTitle = document.createElement("div");
  cardInfoTitle.setAttribute("class", "first_line_text");
  cardInfoTitle.textContent = "信用卡付款資訊";
  let numContainer = document.createElement("div");
  numContainer.className = "numContainer";
  let cardNum = document.createElement("div");
  cardNum.id = "card-number";
  let cardNumLabel = document.createElement("label");
  cardNumLabel.textContent = "卡片號碼：";
  let expirationContainer = document.createElement("div");
  expirationContainer.className = "expirationContainer";
  let expiration = document.createElement("div");
  expiration.id = "card-expiration-date";
  let expirationLabel = document.createElement("label");
  expirationLabel.textContent = "過期時間：";
  let ccvContainer = document.createElement("div");
  ccvContainer.className = "ccvContainer";
  let ccv = document.createElement("div");
  ccv.id = "card-ccv";
  let ccvLabel = document.createElement("label");
  ccvLabel.textContent = "驗證密碼：";
  main.appendChild(line);
  main.appendChild(mainLayout);
  mainLayout.appendChild(cardInfoTitle);
  mainLayout.appendChild(numContainer);
  mainLayout.appendChild(expirationContainer);
  mainLayout.appendChild(ccvContainer);
  numContainer.appendChild(cardNumLabel);
  numContainer.appendChild(cardNum);
  expirationContainer.appendChild(expirationLabel);
  expirationContainer.appendChild(expiration);
  ccvContainer.appendChild(ccvLabel);
  ccvContainer.appendChild(ccv);
}
